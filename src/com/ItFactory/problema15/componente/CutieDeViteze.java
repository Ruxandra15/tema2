package com.ItFactory.problema15.componente;

public class CutieDeViteze {
    private String marcaProducitie;
    private double pretVanzare;
    private boolean automata;

    public String getMarcaProducitie() {
        return marcaProducitie;
    }

    public void setMarcaProducitie(String marcaProducitie) {
        this.marcaProducitie = marcaProducitie;
    }

    public double getPretVanzare() {
        return pretVanzare;
    }

    public void setPretVanzare(double pretVanzare) {
        this.pretVanzare = pretVanzare;
    }

    public boolean isAutomata() {
        return automata;
    }

    public void setAutomata(boolean automata) {
        this.automata = automata;
    }
}
